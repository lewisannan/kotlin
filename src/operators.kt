fun main() {
    //arithmetic operators
    var x = 300
    var y = 5
    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y)

    //comparison operators
    println(x < y)
    println(x > y)
    println(x <= y)
    println(x >= y)
    println(x == y)
    println(x != y)

    //assignment operators
    var number = 10
    println(number)

    number += 3
    println(number)

    //logical operators - &&(and), ||(or), !(not)
    println(400 > 200 && 50 == 25)
    println(400 > 200 || 50 == 25)
    println(!(400 > 200 || 50 == 25))



}